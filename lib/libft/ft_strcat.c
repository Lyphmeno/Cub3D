#include "libft.h"

void	ft_strcat(char *dst, char *src)
{
	int		i;
	int		j;

	i = 0;
	j = ft_strlen(dst);
	while (src[i])
	{
		dst[j] = src[i];
		i++;
		j++;
	}
	dst[j] = '\0';
}
